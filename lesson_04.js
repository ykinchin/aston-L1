class Person {
	constructor(name) {
		this._name = name
	}

	get name() {
		return this._name
	}

	set name(newName) {
		this._name = newName
	}
}

class PersonThree extends Person {
	constructor(name, age) {
		super(name)
		this.age = age
	}
}
